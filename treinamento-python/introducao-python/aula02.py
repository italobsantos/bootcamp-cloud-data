

#Interação com o usuario:
a = int(input('Entre o primeiro valor: '))
b = int(input("Entre o segundo valor: "))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b

#Formatando o saída do print
print('Soma: {}'.format(soma))
print('Subtração: {}'.format(subtracao))
print('Multiplicação: {}'.format(multiplicacao))
print('Divisão: {}'.format(divisao))
print('Resto: {}'.format(resto))

